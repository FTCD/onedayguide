package main.java.entities;

public class NivelIdioma {

	String cod;
	String descCastellano;
	String descEuskera;
	String descIngles;
	
	public String getCod() {
		return cod;
	}
	
	public void setCod(String cod) {
		this.cod = cod;
	}

	public String getDescCastellano() {
		return descCastellano;
	}

	public void setDescCastellano(String descCastellano) {
		this.descCastellano = descCastellano;
	}

	public String getDescEuskera() {
		return descEuskera;
	}

	public void setDescEuskera(String descEuskera) {
		this.descEuskera = descEuskera;
	}

	public String getDescIngles() {
		return descIngles;
	}

	public void setDescIngles(String descIngles) {
		this.descIngles = descIngles;
	}
	
}